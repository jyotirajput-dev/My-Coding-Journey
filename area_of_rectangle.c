#include <stdio.h>

/*
    Program: Area of Rectangle
    Description:
    This program calculates the area of a rectangle
    using the formula: Area = Length × Breadth
*/

int main()
{
    int length = 98;
    int breadth = 65;

    int area = length * breadth;

    printf("The area of the rectangle is %d\n", area);

    return 0;
}